dengan Heteractis magnefica. Pemeliharaan ikan Amphiprion oscellaris di akurium tidak membutuhkan anemon laut 
untuk bertahan hidup, sehingga penggunaan anemon laut dapat diminimalisir dan mengganti dengan anemon 
buatan sebagai hiasan akuaska
