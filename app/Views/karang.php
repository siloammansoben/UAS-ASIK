  <h2>Terumbu karang</h2>
  <p>Sumber : https://repository.unimal.ac.id/4884/1/%5BNabil%20Zurba%5D%20Terumbu%20Karang.pdf<p>
  <p>
  Terumbu karang merupakan salah satu ekosistem khas pada 
perairan pesisir di wilayah tropis. Terdapat dua penjelasan khusus 
terhadap terumbu dan karang, yang mana kedua buah kata ini bukan 
merupakan satu kesatuan, melainkan penggabungan kata dari 
terumbu dan karang yang akan dijelaskan pada ulasan dibawah ini. 
Karang merupakan individu-individu berukuran kecil yang disebut 
polip. Setiap polip seperti kantung berisi air yang dilengkapi dengan 
lingkaran tentakel yang mengelilingi mulutnya, dan terlihat seperti 
anemon kecil. Polip di dalam koloni terhubungkan oleh jaringan 
hidup dan dapat berbagi makanan. Sedangkan terumbu karang 
adalah struktur di dasar laut berupa deposit kalsium karbonat yang 
dihasilkan terutama oleh hewan karang. Karang adalah hewan tak 
bertulang belakang yang termasuk dalam Filum Coelenterata (hewan 
berrongga) atau Cnidaria.
Binatang karang terlihat seperti tanaman, padahal sebenarnya 
karang merupakan sekumpulan hewan-hewan kecil yang bernama 
polip. Orang yang pertama kali mengklasifikasikan karang sebagai 
binatang adalah J.A. de Peysonell, seorang ahli biologi dari Perancis 
pada tahun 1753. Dalam klasifikasi ilmiah, karang berada dalam 
filum Cnidaria, kelas Anthozoa. Terumbu karang (coral reefs) juga 
merupakan kumpulan masyarakat (binatang) karang, yang hidup di 
dasar perairan, yang berupa batuan kapur (CaCO3), dan mempunyai 
kemampuan yang cukup kuat untuk menahan gaya gelombang laut. 
Terumbu terbentuk dari endapan-endapan masif kalsium karbonat 
yang dihasillkan oleh organisme karang pembentuk terumbu (karang 
hermatifik) dari filum Cnidaria, ordo Sclerectinia yang hidup 
bersimbiosis dengan alga zooxanthellae dan sedikit tambahan alga 
berkapur dan organisme lain yang mengsekresi kalsium karbonat.

</p>
<br>
<p>
Secara harfiah Terumbu karang dapat dijelaskan sebagai 
struktur di dasar laut berupa deposit kalsium karbonat di laut yang 
dihasilkan terutama oleh hewan karang. Karang adalah hewan tak 
bertulang belakang yang termasuk dalam Filum Coelenterata (hewan 
berrongga) atau Cnidaria. Yang disebut sebagai karang (coral) 
mencakup karang dari Ordo scleractinia dan Sub kelas Octocorallia
(kelas Anthozoa) maupun kelas Hydrozoa. Satu individu karang atau 
disebut polip karang memiliki ukuran yang bervariasi mulai dari 
yang sangat kecil 1 mm hingga yang sangat besar yaitu lebih dari 50 
 Namun yang pada umumnya polip karang berukuran kecil. Polip 
dengan ukuran besar dijumpai pada karang yang soliter.
Terumbu karang merupakan salah satu ekosistem yang 
berperan penting pada wilayah pesisir namun rentan terhadap 
perubahan baik yang terjadi secara internal maupun ekternal. 
Sedimentasi merupakan salah satu faktor pembatas kehidupan 
binatang karang. Luas tutupan karang mempengaruhi struktur 
komunitas lain yang berasosiasi dengan terumbu karang salah 
satunya yaitu ikan karang. Keragaman ekosistem merupakan salah 
satu indikator penting dalam kelestarian suatu wilayah. Wilayah 
yang memiliki keragaman lebih memiliki ketahanan (reseilience) 
dalam perannya melindungi kawasan pesisir. Keberadaan spesies 
yang dilindungi pada suatu kawasan merupakan salah satu urgensi 
dalam pengelolaan kawasan konservasi, sehingga eksistensinya 
dapat dipertahankan dan terhindar dari kepunahan. Hamparan 
terumbu karang dapat dilihat pada Gambar 1 di bawah ini.
</p>
<br>
<p>
Karang tergolong dalam jenis makhluk hidup (hewan) yaitu 
sebagai individu organisme atau komponen dari masyarakat hewan. 
Dalam bentuk yang paling sederhana, karang hanya bisa terdiri dari 
sebuah polip yang mempunyai bentuk seperti tabung dengan mulut 
di bagian atas yang dikelilingi oleh tentakel. Karang termasuk dalam 
Universitas Malikussaleh 3
filum Cnidaria, yaitu organisme yang memiliki penyengat. Hewan 
karang adalah hewan sessile renik, umumnya berada dalam 
ekosistem bersama hewan laut lain seperti soft coral, hydra, anemone 
laut dan lain-lain yang termasuk ke dalam Phylum Cnidaria 
(Coelenterata).
Secara umum terdapat dua kelompok Cnidaria, yaitu Hydrozoa
dan Anthozoa. Hydrozoa terdiri dari Millepora dan Stylasterina. 
Stylasterina biasanya kecil dan hidup di tempat yang tersembunyi di 
dinding gua dan bukan merupakan karang pembentuk terumbu. 
Anthozoa yang umum dikenal adalah: Stolonifera, contohnya 
Tubipora musica; Coenothecalia, contohnya Heliopora coeruela. 
Sclerectinia atau lebih dikenal sebagai karang keras yang meliputi 
jenis-jenis karang pembentuk karang utama. Morfologi dan Fisiologi.
