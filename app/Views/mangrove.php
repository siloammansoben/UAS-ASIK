2.2 Peranan dan Fungsi Ekosistem 
Mangrove 
 Menurut Saenger (1981) dalam 
Anwar, dkk (1984) salah satu fungsi 
ekosistem mangrove adalah sebagai Fungsi 
biologi, yaitu sebagai dearah pasca larva dan 
yuwana jenis jenis ikan tertentu dan menjadi 
habitat alami berbagai jenis biota dengan 
produktivitas yang tinggi, serta 
bersarangnya burung-burung besar. 
Ekosistem mangrove merupakan 
ekosistem peralihan antara darat dan laut 
yang dikenal memiliki peran dan fungsi sangat 
besar. Secara ekologis mangrove memiliki 
fungsi yang sangat penting dalam 
memainkan peranan sebagai mata rantai 
makanan di suatu perairan, yang dapat 
menumpang kehidupan berbagai jenis ikan, 
udang dan moluska. Perlu diketahui bahwa 
hutan mangrove tidak hanya melengkapi 
pangan bagi biota aquatik saja, akan tetapi 
juga dapat menciptakan suasana iklim yang 
kondusif bagi kehidupan biota aquatik, serta 
memiliki kontribusi terhadap 
keseimbangan siklus biologi di suatu 
perairan. Kekhasan tipe perakaran beberapa 
jenis tumbuhan mangrove seperti Rhizophora 
sp., Avicennia sp. dan Sonneratia sp. dan 
kondisi lantai hutan, kubangan serta aluralur yang saling berhubungan merupakan 
perlidungan bagi larva berbagai biota laut. 
Kondisi seperti ini juga sangat penting dalam 
menyediakan tempat untuk bertelur, 
pemijahan dan pembesarkan serta tempat 
mencari makan berbagai macam ikan dan 
udang kecil, karena suplai makanannya 
tersedia dan terlindung dari ikan pemangsa. 
Ekosistem mangrove juga berperan sebagai 
habitat bagi jenis-jenis ikan, kepiting dan 
kerang-kerangan yang mempunyai nilai 
ekonomi tinggi. 
Dilihat dari aspek fisik, hutan 
