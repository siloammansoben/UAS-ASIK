  Indonesia merupakan suatu negara 
kepulauan yang terdiri dari 13.667 pulau 
dan mempunyai wilayah pantai sepanjang 
54.716 kilometer.Wilayah pantai (pesisir) 
ini banyak ditumbuhi hutan mangrove. Luas 
hutan mangrove di Indonesia sekitar 
4.251.011,03 hektar dengan penyebaran: 
15,46 persen di Sumatera, 2,35 persen di 
Sulawesi, 2,35 persen di Maluku, 9,02 
persen di Kalimantan, 1,03 persen di Jawa, 
0,18 persendi Bali dan Nusa Tenggara, dan 
69,43 persen di Irian Jaya (Fao, 1990 dalam 
Hainim, 1996). Namun, menurut Cifor 
(2012), luas hutan mangrove di Indonesia 
telah mengalami penurunan 30-50% pada 
setengah abad terakhir ini karena 
pembangunan daerah pesisir, perluasan 
pembangunan tambak, abarasi air laut, dan 
penebangan yang berlebihan. Sedangkan 
berdasarkan data Kementrian Kehutanan 
(2013), Hutan mangrove di Indonesia 
tersebar di beberapa provinsi di berbagai 
gugusan kepulauan. Luasan hutan mangrove 
di Indonesia lebih kurang 3,7 juta hektar 
yang merupakan hutan mangrove terluas 
yang ada di Asia dan bahkan di dunia. 
Menurut catatan Statistik Kehutanan Tahun 
1993 NTB mempunyai luas kawasan hutan 
sebesar 1.063.273,20 ha., dan 160.878,50 ha 
(15,13 %) berada di pulau Lombok. Luas 
hutan mangrove dalam Statistik Kehutanan 
NTB Tahun 1993, sebesar 49.174 ha. 
(Chaniago dan Hayashi , 1994). 
 Menurut Imran (2016), ekosistem 
hutan mangrove merupakan salah satu 
ekosistem yang memiliki produktivitas 
tinggi dibandingkan ekosistem lain dengan 
dekomposisi bahan organik yang tinggi, dan 
menjadikannya sebagai mata rantai ekologis 
yang sangat penting bagi kehidupan mahluk 
hidup yang berada di perairan sekitarnya. 
Materi organik menjadikan hutan mangrove 
sebagai tempat sumber makanan dan 
tempatasuhan berbagai biota seperti ikan, 
udang dan kepiting. Produksi ikan dan 
udang di perairan laut sangat bergantung 
dengan produksi serasah yang dihasilkan 
oleh hutan mangrove. Berbagai kelompok 
moluska ekonomis juga sering ditemukan 
berasosiasi dengan tumbuhan penyusun 
hutan mangrove. Sedangkan Bruno, dkk
(1998) menyatakan bahwa hutan mangrove 
merupakan jenis maupun komunitas 
tumbuhan yang tumbuh di daerah pasang 
surut. Selain itu, hutan mangrove 
mempunyai karakteristik yang unik 
dibandingkan dengan formasi hutan lainnya. 
Keunikan hutan tersebut terlihat dari habitat 
tempat hidupnya, juga keanekaragaman 
flora, yaitu: Avicennia, Rhizophora, 
Bruguiera, dan tumbuhan lainnya yang 
mampu bertahan hidup disalinitas air laut, 
dan fauna yaitu kepiting, ikan, jenis 
Molusca, dan lain-lain. Hutan mangrove 
juga memiliki fungsi ekonomi, ekologi, dan 
sosial. Fungsi ekonomi yang ada di hutan 
mangrove yaitu penghasil kebutuhan rumah 
tangga, penghasil keperluan industri, dan 
penghasil bibit. Fungsi ekologisnya yaitu 
sebagai pelindung garis pantai, mencegah 
intrusi air laut, sebagai habitat berbagai jenis 
burung, dan lain-lain (Kustanti, 2011). 
Namun Saenger et al. (1983) menyatakan 
bahwa, jenis tumbuhan mangrove di seluruh 
dunia adalah sekitar 60 jenis. 
