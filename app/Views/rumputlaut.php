Rumput laut memiliki banyak peranan penting bagi manusia. 
Ilalqisny dan Widyartini (2000) melaporkan bahwa sejak tahun 2700 SM, 
rumput laut telah dimanfaatkan sebagai bahan pangan manusia. Perancis, 
Normandia, dan Inggris pada abad 17 mulai merintis pemanfaatan rumput 
laut untuk pembuatan gelas (Soegiarto et al., 1978). Namun, pemanfaatan 
rumput laut secara ekonomis baru dimulai tahun 1670 di Cina dan Jepang, 
yaitu sebagai bahan obat-obatan, makanan tambahan, kosmetika, pakan 
ternak, dan pupuk organik. Pada tahun 2005 dilaporkan bahwa konsumsi 
rumput laut bagi masyarakat Cina, Jepang, dan Korea mencapai 2 milyar US
$. Setiap hari sekitar 168 spesies alga telah dikomersilkan, di Jepang, Cina, 
Taiwan, dan Korea, diantaranya porphyra (nori), laminaria (kombu), 
