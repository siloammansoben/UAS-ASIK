  <p>
  Rumput laut atau lebih dikenal dengan sebutan seaweed merupakan 
salah satu sumber daya hayati yang sangat melimpah di perairan Indonesia. 
Keanekaragaman rumput laut di Indonesia merupakan yang terbesar 
dibandingkan dengan negara lain. Namun demikian, pemanfaatan rumput 
laut di Indonesia, terutama untuk keperluan industri dan kesehatan masih 
belum optimal. Review ini bermaksud memberikan informasi mengenai 
kajian pemanfaatan sumber daya rumput laut dari aspek industri dan 
kesehatan, sehingga diharapkan dapat menambah khasanah keanekaragaman 
makanan fungsional yang bermanfaat bagi kesehatan dan memantapkan 
pemanfaatannya di bidang industri di Indonesia. Optimalisasi upaya 
penggalian potensi sumber daya rumput laut di Indonesia perlu 
dikembangkan dalam rangka mendukung upaya pemecahan persoalan 
bangsa ini khususnya mengahadapi krisis ekonomi global dan meningkatnya 
kasus gizi buruk di Indonesia.
Rumput laut ini merupakan salah satu kelompok tumbuhan laut yang 
mempunyai sifat tidak bisa dibedakan antara bagian akar, batang, dan daun. 
Seluruh bagian tumbuhan disebut thallus, sehingga rumput laut tergolong 
tumbuhan tingkat rendah (Susanto&Mucktianty, 2002). Bentuk thallus
rumput laut bermacam-macam, ada yang bulat seperti tabung, pipih, gepeng, 
