undaria (wakame). Porphyra atau nori merupakan rumput laut yang adalah 
yang paling populer di Jepang (Steinman, 2006). Contoh makanan yang 
terbuat dari rumput laut terkenal di Jepang adalah Kombu. Kombu terbuat 
dari rumput laut jenis Laminaria sp yang termasuk golongan kelp (Anonim, 
2006). Salah satu contoh kelp di Indonesia adalah Sargassum sp. Di 
berbagai belahan dunia, Sargassum sp merupakan jenis rumput laut di 
