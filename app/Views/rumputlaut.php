industri, makanan, tekstil, kertas, cat, kosmetika, dan farmasi (obat-obatan). 
Di Indonesia, pemanfaatan rumput laut untuk industri dimulai untuk industri 
agar-agar (Gelidium dan Gracilaria) kemudian untuk industri kerajinan 
(Eucheuma) serta untuk industri alginat (Sargassum). 
