  <h2>Sargassum</h2>
  <p>Sumber : https://media.neliti.com/media/publications/75196-ID-none.pdf<p>
  <p>
  Sargassum sp. merupakan alga coklat
yang hidup pada habitat karang dengan
kedalaman 0,5-10 meter. Anggadiredja
(2006) menjelaskan bahwa Sargassum
adalah salah satu genus dari kelompok
rumput laut coklat yang merupakan genera
terbesar dari family Sargassaceae.
Guiry (2007) menjelaskan bahwa
Sargassum sp. adalah rumput laut yang
tergolong Divisi Phaeophyta (ganggang
coklat). Spesies ini dapat tumbuh sampai
panjang 12 meter. Tubuhnya berwarna
cokelat kuning kehijauan, dengan struktur
tubuh terbagi atas sebuah holdfast yang
berfungsi sebagai struktur basal, sebuah stipe
atau batang semu, dan sebuah frond yang
berbentuk seperti daun. Warna coklat pada
alga divisi Phaeophyta muncul akibat
dominansi dari pigmen fucoxanthin, klorofil
a dan c, betakaroten, dan xantofil lainnya.
Karbohidrat yang disimpan sebagian besar
tersedia dalam bentuk laminaran
(polisakarida glukosa; terbentuk dari proses
fotosintesis), disertai dengan pati dalam
jumlah tertentu tergantung spesiesnya.
Dinding selnya terbuat dari selulosa dan
asam alginat. Anggadiredja et al. (2008)
menjelaskan bahwa Sargassum sp. memiliki
bentuk thallus gepeng, banyak percabangan
yang menyerupai pepohonan di darat,
bangun daun melebar, lonjong seperti
pedang, memiliki gelembung udara yang
umumnya soliter, batang utama bulat agak
kasar, dan holdfast (bagian yang digunakan
untuk melekat) berbentuk cakram, pinggir
daun bergerigi jarang, berombak, dan ujung
melengkung atau meruncing.
Sargassum sp. juga berperan penting
dalam ekosistem. Kadi (2005). Ia
menjelaskan bahwa secara ekologis alga
Sargassum ikut andil dalam pembentukan
ekosistem terumbu karang dan merupakan
tempat asuhan bagi biota kecil, termasuk
untuk perlindungan benih ikan dan benur
udang serta sarang melekatnya telur cumi- cumi.
Rachmat (1999) menjelaskan
bahwa Sargassum merupakan alga coklat
yang terdiri dari kurang lebih 400 jenis di
dunia. Jenis-jenis Sargassum yang dikenal di
Indonesia ada sekitar 12 spesies, yaitu :
Sargassum duplicatum, Sargassum histrix, Sargassum echinocarpum, Sargassum
gracilimun, Sargassum obtusifolium,
Sargassum binderi, Sargassum policystum,
Sargassum crassifolium, Sargassum
microphylum, Sargassum aquofilum,
Sargassum vulgare, dan Sargassum
polyceratium. Bold dan Wynne (1977)
menjelaskan bahwa perkembangbiakan atau
reproduksi marga Sargassum yang termasuk
bangsa Fucales, marga Sargassaceae dikenal
dua cara yaitu: Reproduksi asexual
(vegetatif) dan sexual (generatif).
Reproduksi vegetatif dilakukan melalui
fragmentasi yaitu potongan thallus
berkembang melakukan pertumbuhannya.
Cara ini banyak dilakukan untuk usaha
budidaya. Reproduksi generatif yaitu
perkembangan individu melalui organ jantan
(antheridia) dan organ betina (oogenia) .
<br>
<p>
Sargassum sp. dapat tumbuh di
daerah Teluk Ekas Kecamatan Jerowaru
Lombok Timur karena kondisi lingkungan
mendukung untuk pertumbuhannya. Daerah
ini memiliki kawasan pasang surut yang
terbuka dan memiliki substrat pasir
berlumpur serta kedalaman hanya mencapai
2,5 meter saat pasang maksimal. Hal ini
sesuai dengan pernyataan Atmadja dan
Soelistijo (1980) bahwa pada umumnya
Sargassum tumbuh di daerah terumbu karang
(coral reef) seperti di Kepulauan Seribu,
terutama di daerah rataan pasir (sand flat ).
Daerah ini akan kering pada saat surut
rendah, mempunyai dasar berpasir dan
terdapat pula pada karang hidup atau mati.
Pada batu-batu ini tumbuh dan melekat
rumput laut coklat. Pada musim-musim tertentu wilayah
teluk akan mengalami surut yang bahkan
tinggi air hanya mencapai 5 cm, dalam
keadaan demikian para masyarakat seringkali
melakukan perburuan kerang dan rumput
laut yang biasa disebut dengan madak. Boney (1965) mengatakan bahwa
karakteristik biologi rumput laut Sargassum
crassifolium (alga coklat) hidup dan tumbuh
di daerah pesisir pantai dengan substrat batu
karang. Sargassum crassifolium tumbuh di
daerah intertidal, subtidal sampai daerah
dengan ombak besar dan arus yang deras.
Alga ini tumbuh pada daerah tropis dengan
suhu 27-30 oC, salinitas 32-33 ppt dan
kedalaman 0,5-10 m. Menurut Hak dan
Tazwir [2004] mengatakan bahwa selama
dilakukan budidaya rumput laut coklat,
diamati media tumbuh rumput laut coklat
tersebut, suhu air laut rata- rata 29 oC, kecepatan angin 5/min - 5,4/min dan suhu
udara 32-35 oC. Berdasarkan keadaan
wilayah Teluk Ekas, kisaran suhunya antara
29-30 oC, artinya suhu tersebut merupakan
suhu yang dikatakan masih optimal dalam
upaya pembudidayaan Sargassum sp. khususnya dan rumput laut secara umum. Adapun faktor-faktor yang berperan
penting dalam kecepatan tumbuh Sargassum
adalah Intensitas cahaya dan kecepatan arus. Patok dasar cenderung substratnya lebih
mudah keruh karena terjadinya proses
pengadukan sebagai akibat dari kuatnya arus
di dasar perairan. Disamping itu juga karena
rendahnya intensitas cahaya menyebabkan
kemampuan fotosintesisnya jadi lebih
rendah. Apabila dibandingkan dengan rakit
terapung, tentu saja rakit terapung lebih
unggul dalam hal intensitas cahaya karena
berhadapan langsung dengan cahaya
matahari dan lambatnya kecepatan arus dan
tidak bersubstrat pasir berlumpur menjadikan
sistem budidaya ini jauh lebih efektif
daripada sistem budidaya patok dasar.
</p>
