Interaksi faktor biologi yang 
mempengaruhi penyebaran bambu laut di perairan 
Tanjung Tiram yaitu kondisi karang. Penyebaran 
bamboo laut di perairan Tanjung Tiram cenderung 
pada daerah dengan kondisi karang yang sedang. 
Manuputty, (2002) menyatakan sebagai unsur 
