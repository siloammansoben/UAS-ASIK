  <p>
  Bambu laut (Isis hippuris Linnaeus 1758), 
(Octocorallia, Calcaxonia, Isididae), merupakan 
salah satu jenis karang lunak yang hidup di 
perairan tropis Indo-Pasifik. Di Indonesia jenis ini 
mendominasi perairan Indonesia bagian timur,. 
Jenis ini termasuk dalam kelompok gorgonia, yaitu 
kelompok oktokoral yang tumbuh dan muncul dari 
substrat dasar dan mempunyai kerangka dalam 
(axial) yang kokoh. Kerangka (axial) terdiri dari 
gorgonin yang keras dan padat, yang sama dengan 
zat tanduk yang mengandung substansi kollagen 
dan senyawa protein. 
Secara sepintas di dalam air, koloni Bambu 
Laut (I.hippuris) kelihatan mirip dengan koloni 
kelompok akar bahar Rhumpella sp., terutama 
pertumbuhan yang seperti semak dan permukaan 
koloni yang halus. Perbedaan yang khas adalah, 
I.hippuris memiliki percabangan yang cenderung 
ke arah kanan, dan ujung atas koloni yang 
melengkung seperti busur demikian pula ukuran 
dan bentuk cabang-cabang, Rhumpella sp. 
memiliki cabang yang agak panjang, sedang 
I.hippuris lebih pendek dengan ujung cabang lebih 
bulat. Tekstur tubuh dan koloni Rhumpella sp. 
