menurunnya populasi bamboo laut. 
Berdasarkan hal tersebut, mengingat saat ini 
kondisi mengenai ekologi bambu laut khususnya 
dalam jumlah populasi masih belum diketahui dan 
memiliki data yang masih kurang serta 
keberadaannya di perairan Tanjung Tiram belum 
